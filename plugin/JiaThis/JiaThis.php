<!-- 如何自定义分享URL和Title？
说明：Jiathis分享到按钮，在缺省状态下是用JS自动获取当前网页的标题和链接，但是如果你想自己定义新的URL和标题，可以通过修改以下参数实现：
参数url: 定义要分享的链接地址 
参数title: 定义要分享页面的标题
参数summary: 定义要分享页面的摘要，摘要默认为Meta标签中Description部分 -->
<!-- JiaThis Button BEGIN --> 
<script type="text/javascript">
var jiathis_config = { 
	url: "http://www.yourdomain.com", 
	title: "自定义网页标题 #微博话题#", 
	summary:"分享的文本摘要",
	shortUrl:false,
	hideMore:false
}
</script>
<script src="http://v2.jiathis.com/code/jiathis_r.js?move=0"></script> 
<!-- JiaThis Button END -->



<!-- 一、动态自定义Title
一般情况下，JiaThis分享工具默认抓取当前页面的标题。如果用户想抓取动态页面中的文章标题，需要找到文章标题DIV的ID值。例如，测试文章标题所在DIV的ID值为article_title ，添加方法如图所示：-->
<!-- JiaThis Button BEGIN -->
<script>
var jiathis_config = {
title:document.getElementById("article_title").innerHTML
}
</script>
<!-- JiaThis Button END -->



<!-- 二、动态自定义Summary
同样，JiaThis分享工具默认获取页面<meta>标签的内容作为分享摘要。如果用户想抓取动态页面中的Summary，需要找到文章摘要DIV的ID值。例如，测试文章摘要所在DIV的ID值为description ，添加方法如图所示：  -->
<!-- JiaThis Button BEGIN -->
<script>
var jiathis_config = {
url:"http://www.jiathis.com",
title:document.getElementById("article_title").innerHTML,
summary:document.getElementById("description").innerHTML
}
</script>
<!-- JiaThis Button END --> 


